public class Model {
	
	 private  PlayerObject Player;
	 private Controller controller = Controller.getInstance();
	 private  CopyOnWriteArrayList<EnemyObject> EnemiesList  = new CopyOnWriteArrayList<EnemyObject>();
	 private  CopyOnWriteArrayList<BulletObject> BulletList  = new CopyOnWriteArrayList<BulletObject>();
	 private  CopyOnWriteArrayList<FloorObject> FloorList  = new CopyOnWriteArrayList<FloorObject>();
	 long startTime = System.currentTimeMillis();

	private float gravity=(float) -.01;
	 private int Score=0; 
		public Model() {
		 //setup game world 
		//Player 
		Player= new PlayerObject(50,50,new Point3f(500,600,0));
		//Enemies  starting with four 
		EnemiesList.add(new EnemyObject(80,80,new Point3f(60,60,0))); 

		FloorList.add(new FloorObject(40,new Point3f(0,1000,0)));

		for(int i=0;i<1500;i+=100){
		FloorList.add(new FloorObject((int)((5.0 * Math.random())+1),new Point3f((int)(600.0 * Math.random()),((int)(i-100* Math.random()-500)),0)));
		}
		

	
		
	    
	}
	
	// This is the heart of the game , where the model takes in all the inputs ,decides the outcomes and then changes the model accordingly. 
	public void gamelogic() 
	{
		// Player Logic first 
		playerLogic(); 
		floorLogic();
		gravityLogic();
		// Enemy Logic next
		enemyLogic();
		// Bullets move next 
		bulletLogic();
		// interactions between objects 
		gameLogic(); 
		if(!Player.isGrounded()){
		gravity=(float) -.5;}

		Player.setYVelocity(Player.getVelocity()+gravity);

		
	




	}

	private void gameLogic() { 
		
		
		// this is a way to increment across the array list data structure 

		
		//see if they hit anything 
		// using enhanced for-loop style as it makes it alot easier both code wise and reading wise too 
		
		for (BulletObject Bullet : BulletList) 
		{
			if ( Math.abs(Player.getCentre().getX()- Bullet.getCentre().getX())< Player.getWidth() 
				&& Math.abs(Player.getCentre().getY()- Bullet.getCentre().getY()) < Player.getHeight())
			{ 
				BulletList.remove(Bullet);
				Player.setLives(Player.getLives()-1);
				 
			}  
		}
		
		
	}
	private void floorLogic() {
		//makes the floors move the right way 
		for(FloorObject floor:FloorList){
			floor.getCentre().setY(floor.getCentre().getY()+Player.getVelocity());	
		}

		Collections.sort(FloorList,new SortByY());
		if(FloorList.size()<15){
			System.out.println("new floor object");
		FloorList.add(new FloorObject((int)(10.0 * Math.random()),new Point3f((int)(600.0 * Math.random()),-100,0)));
		}
		

		





		if(Player.getVelocity()<=0){
		boolean grounded=false;
		
		for(FloorObject floor:FloorList){
			if(floor.getCentre().getY()>1500){
				FloorList.remove(floor);
				FloorList.add(new FloorObject((int)(10.0 * Math.random()),new Point3f((int)(900.0 * Math.random()),floor.getCentre().getY()-1600,0)));
			}
			if ((floor.getCentre().getY()-32-Player.getCentre().getY()<20&&floor.getCentre().getY()-32-Player.getCentre().getY()>-20)&&(floor.getCentre().getX()-Player.getCentre().getX()<15&&floor.getCentre().getX()-Player.getCentre().getX()>-28*floor.getNumBlocks())){
				grounded=true;
				Player.getCentre().setY(floor.getCentre().getY()-40);
				Player.setYVelocity(0);
				gravity=0;
				break;

			}
			else{
				grounded=false;
				
			}
			
			
		}
		Player.setGrounded(grounded);
	}
		
		
	}

	private void enemyLogic() {
		// TODO Auto-generated method stub
		
		for(EnemyObject enemy:EnemiesList){
			enemy.getCentre().setY(enemy.getCentre().getY()+Player.getVelocity());
			
		}

		long elapsedTime = System.currentTimeMillis() - startTime;
		long elapsedSeconds = elapsedTime / 1000;
		for (EnemyObject enemy:EnemiesList){
				if(elapsedSeconds>3){
					startTime = System.currentTimeMillis();
					System.out.println(Player.getCentre().getX()-enemy.getCentre().getX());
					System.out.println(Player.getCentre().getY()-enemy.getCentre().getY());
					float x1=(Player.getCentre().getX()-enemy.getCentre().getX());
					float y1=(Player.getCentre().getY()-enemy.getCentre().getY());
					float magnitude=(float) Math.sqrt(Math.pow(x1,2.0)+Math.pow(y1,2.0));
					CreateBullet(enemy.getCentre().getX(),enemy.getCentre().getY(),new Vector3f(3*x1/magnitude,-3*y1/magnitude,0));
				}
	}

	
	}

	private void bulletLogic() {
		// TODO Auto-generated method stub
		// move bullets 
		for(BulletObject bullet:BulletList){
			bullet.getCentre().setY(bullet.getCentre().getY()+Player.getVelocity());	
		}
		for (BulletObject temp : BulletList) 
		{
		    //check to move them
			  if(temp.getCentre().getX()<50||temp.getCentre().getX()>950||temp.getCentre().getY()>900||temp.getCentre().getY()<50){
				  BulletList.remove(temp);
			  }
			temp.getCentre().ApplyVector(temp.getVector());
			//see if they hit anything 
			
			//see if they get to the top of the screen ( remember 0 is the top 
			
		} 
		
	}

	private void playerLogic() {
		
		// smoother animation is possible if we make a target position  // done but may try to change things for students  
		 
		//check for movement and if you fired a bullet 
		  
		if(Controller.getInstance().isKeyAPressed()){Player.getCentre().ApplyVector( new Vector3f(-4,0,0)); }
		
		if(Controller.getInstance().isKeyDPressed())
		{
			Player.getCentre().ApplyVector( new Vector3f(4,0,0));
		}
			
	
		
		if(Controller.getInstance().isKeySpacePressed())
		{			
			Jump();
			Controller.getInstance().setKeySpacePressed(false);
		} 
		
		
	}
	private void gravityLogic(){
		if(Player.isGrounded()&&Player.getVelocity()<0){Player.setYVelocity(0);}
		
		else if(Player.getCentre().getY()<600) {
				Player.setYVelocity(Player.getVelocity()+gravity);			
		}
	
	
		}
		private void CreateBullet(float x, float y,Vector3f vector) {
			BulletList.add(new BulletObject("D:/Downloads/gameassignment1/game/res/Bullet.png", 32, 64, new Point3f(x,y, 0.0f),vector));
			
		}
		private void Jump() {
			if(Player.getVelocity()<=0){
			Player.setYVelocity(+20);
			Player.setGrounded(false);
			}
		}

	public PlayerObject getPlayer() {
		return Player;
	}

	public CopyOnWriteArrayList<EnemyObject> getEnemies() {
		return EnemiesList;
	}
	
	public CopyOnWriteArrayList<BulletObject> getBullets() {
		return BulletList;
	}

	public int getScore() { 
		return Score;
	}

	public CopyOnWriteArrayList<FloorObject> getFloorList() {
		return FloorList;
	}

	public void setFloorList(CopyOnWriteArrayList<FloorObject> floorList) {
		FloorList = floorList;
	}
	class SortByY implements Comparator<FloorObject> 
	{ 
		// Used for sorting in ascending order of 
		// roll number 
		public int compare(FloorObject a, FloorObject b) 
		{ 
			return (int) ( b.getCentre().getY()-a.getCentre().getY() );
		} 
	} 

}


